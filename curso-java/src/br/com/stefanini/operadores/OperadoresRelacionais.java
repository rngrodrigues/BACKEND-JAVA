package br.com.stefanini.operadores;

public class OperadoresRelacionais {
	
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		
		// != A diferente B
		// == A igual B
		// < A menor B
		// > A maior B
		// <= A menor ou igual B
		// >= A maior ou igual B
		
		System.out.println(a == b);
	}

}
